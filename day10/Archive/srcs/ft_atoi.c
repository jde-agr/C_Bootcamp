#include "../header/do_op.h"

int		ft_atoi(char *str)
{
	int k;
	int n;

	k = 0;
	while (*str < 33)
		str++;
	if (*str == '-')
		n = 1;
	while (*str == '+' || *str == '-')
	{
		n += 1;
		str++;
	}
	while (*str <= '9' && *str >= '0')
	{
		k = k * 10 + (*str) - '0';
		str++;
	}
	if (n == 3)
		return (k);
	return (n == 1) ? (-k) : (k);
}
