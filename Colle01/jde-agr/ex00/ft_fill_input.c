void	ft_fill_input(int **puzzle, char **argv)
{
	int i;
	int j;

	i = 1;
	while (argv[i] != '\0')
	{
		j = 0;
		while (argv[i][j] != '\0' && i < 10)
		{
			if (argv[i][j] == '.')
				argv[i][j] = '0';
			if (argv[i][j] >= '0' && argv[i][j] <= '9')
				puzzle[i - 1][j] = argv[i][j] - 48;
			else
				puzzle[i - 1][j] = argv[i][j];
			j++;
		}
		i++;
	}
}
