void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar(45);
		ft_putchar(48 + (-1 * nb));
	}
	else
		ft_putchar(48 + nb);
}
